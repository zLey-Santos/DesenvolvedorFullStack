let fruta = prompt('Escolha uma fruta disponivel, laranja, maça, banana.');

if (fruta === 'laranja') {
    alert('Você escolheu ' + fruta + ', não esqueça de descascar!');

} else if (fruta === 'maça') {
    alert('Você escolheu ' + fruta + ', se você tirar a casca perderá as vitaminas!');

} else if (fruta === 'banana') {
    alert('Você escolheu ' + fruta + ', já vem naturalmente embalada e pronta para o consumo!');
} else {
    alert('Não tenho nada a dizer sobre isso...')
}







